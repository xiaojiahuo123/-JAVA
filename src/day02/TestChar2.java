package day02;

public class TestChar2 {
    public static void main(String[] args) {
        char c1 = 'a';//以字符本身来表示
        char c2 = 97;//用十进制Unicode编码值
        char c3 = '\u0061';//以十六进制的Unicode编码值
        System.out.println("c1 = " +c1);
        System.out.println("c2 = " + c2);
        System.out.println("c3 = " + c3);
    }
}
