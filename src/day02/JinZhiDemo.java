package day02;

public class JinZhiDemo {
    public static void main(String[] args) {
        System.out.println(10);//十进制
        System.out.println(0B10);//二进制，输出的是十进制结果
        System.out.println(010);//八进制，输出的是十进制结果
        System.out.println(0x10);//十六进制，输出的是十进制结果

        System.out.println(25);//十进制
        //System.out.println(0B25);//二进制，错误，二进制只有0-1
        System.out.println(025);//八进制
        System.out.println(0x25);//十六进制
    }
}
